import mongoose from "mongoose";
import bcrypt from "bcrypt";
import jwt from "jsonwebtoken";

const userSchema = new mongoose.Schema({
    firstName: {
        type: String,
        minLength: 4,
        maxLength: 10
    },
    lastName: {
        type: String,
    },
    username: {
        type: String,
    },
    emailId: {
        type: String,
        required: true,
        unique: true,
        lowercase: true,
        trim: true,
    },
    password: {
        type: String,
        required: true,
    },
    age: {
        type: Number,
    },
    gender: {
        type: String,
        enum: {
            values: ['male', 'female', 'other'],
            message: '{VALUE} is not supported'
        },
        required: true,
    },
    photourl: {
        type: String,
        default: "https://images.app.goo.gl/fx79WosVnmDFnnLk8",
    },
    bio: {
        type: String,
        default: "This is a default bio for user",
    },
    domain: {
        type: String,
    },
    techStack: {
        type: [String],
    },
    experienceLevel: {
        type: String,
        default: "beginner",
        validate(value) {
            if(!["beginner", "intermediate", "advanced"].includes(value)) {
                throw new Error("Enter Valid Experience Level")
            }
        }
    },
    timezone: {
        type: String,
        default: "Asia/Kolkata",
    },
    linkedIn: {
        type: String,
        validate(value) {
            if(!value.startsWith("https://www.linkedin.com/")) {
                throw new Error("Enter Valid LinkedIn URL")
            }
        }
    },
    Github: {
        type: String,
    },
},{timestamps: true} );


userSchema.methods.validatePassword = async function(EnteredPassword) {
    const user = this;
    const isMatch = await bcrypt.compare(EnteredPassword, user.password);
    return isMatch;
}

userSchema.methods.getJwtToken = async function() {
    const user = this;
    const token = jwt.sign({userId: user._id}, 'Rahul@2#$%^&*3');
    return token;
}

export const UserModel = mongoose.model("User", userSchema);